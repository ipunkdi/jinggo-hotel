                        <script>
                            document.addEventListener('DOMContentLoaded', function() {
                                var searchInput = document.getElementById('simple-search');
                                
                                if (searchInput) {
                                    // Fokuskan input pencarian saat halaman dimuat
                                    searchInput.focus();
                                    
                                    // Simpan panjang teks saat ini
                                    var currentLength = searchInput.value.length;
                        
                                    // Handler untuk menyimpan dan mengatur seleksi teks
                                    function setSelectionRange(input, selectionStart, selectionEnd) {
                                        if (input.setSelectionRange) {
                                            input.focus();
                                            input.setSelectionRange(selectionStart, selectionEnd);
                                        } else if (input.createTextRange) {
                                            var range = input.createTextRange();
                                            range.collapse(true);
                                            range.moveEnd('character', selectionEnd);
                                            range.moveStart('character', selectionStart);
                                            range.select();
                                        }
                                    }
                        
                                    // Fungsi untuk mempertahankan seleksi teks pada input
                                    function maintainSelection() {
                                        var searchValue = searchInput.value.trim();
                        
                                        // Simpan posisi kursor saat ini
                                        var selectionStart = searchInput.selectionStart;
                                        var selectionEnd = searchInput.selectionEnd;
                        
                                        // Navigasi ke halaman pencarian hanya jika ada nilai pencarian
                                        if (searchValue.length > 0) {
                                            window.location.href = '/room/units?search=' + encodeURIComponent(searchValue);
                                        } else {
                                            // Jika input kosong, navigasi ke halaman utama atau kosongkan hasil pencarian
                                            window.location.href = '/room/units';
                                        }
                        
                                        // Tetapkan kembali seleksi teks setelah navigasi
                                        setTimeout(function() {
                                            setSelectionRange(searchInput, selectionStart, selectionEnd);
                                        });
                                    }
                        
                                    // Menangani event input pada input pencarian
                                    searchInput.addEventListener('input', function() {
                                        maintainSelection();
                                    });
                                }
                            });
